package day09_IfStatements;

public class MaxNumber {

    public static void main(String[] args) {

        int n1 = 10;
        int n2 = 20;

        if (n1 > n2){
            System.out.println(n1 + " is the max number");
        }else{
            System.out.println(n2 + " is the max number");
        }

    }

}
